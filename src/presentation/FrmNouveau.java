    private Date valideDate(String date){
        SimpleDateFormat f = new SimpleDateFormat("dd.MM.yyyy");
        Date d = new Date();
        try {
            d = f.parse(date);
            return d;
        } catch (ParseException ex) {
            return null;
        }
    }
    
    private void checkSave(){
        if(tfNom.getText().length()>0 && tfPrenom.getText().length()>0 && tfDateEngagement.getText().length()>0){
            String dateEngagement = tfDateEngagement.getText();
            Date valid = valideDate(dateEngagement);
            if(valid!=null && valid.before(new Date()) ){
                btnEnregistrer.setEnabled(true); 
            }else{
                btnEnregistrer.setEnabled(false);
            }
        }else{
            btnEnregistrer.setEnabled(false);   
        }
    }
    
    
