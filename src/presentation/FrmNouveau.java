    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        String nom = tfNom.getText();
        String prenom = tfPrenom.getText();
        String dateEngagement = tfDateEngagement.getText();
        int nomFonction = chFonctions.getSelectedIndex();
        int nomDepartement = chDepartements.getSelectedIndex();
        SimpleDateFormat f = new SimpleDateFormat("dd-MM-yyyy");
        Date d = new Date();
        try {
            d = f.parse(dateEngagement);
        } catch (ParseException ex) {
            Logger.getLogger(FrmNouveau.class.getName()).log(Level.SEVERE, null, ex);
        }
        Date dateEngagementD = d;
        EmployeDao.saveEmploye(nom, prenom, dateEngagementD, nomFonction, nomDepartement);
        frmMain.resetEmployes(nom + " " + prenom);        
        dispose();
    }//GEN-LAST:event_btnEnregistrerActionPerformed

